#include <stdlib.h>
/**
 * op_add - adds two ints
 * @a: int a
 * @b: int b
 *
 * Return: sum of them
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 *op_sub - subtracts two ints
 * @a: int a
 * @b: int b
 *
 * Return: result of subtraction
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiplies two ints
 * @a: int a
 * @b: int b
 *
 * Return: product of two ints
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divides two ints
 * @a: int a
 * @b: int b
 *
 * Return: quotient of two ints
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - mod of ints
 * @a: int a
 * @b: int b
 *
 * Return: remainder of two integers
 */
int op_mod(int a, int b)
{
	return (a % b);
}
